    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        if (txtCedula.getText().equals("")||txtNombre.getText().equals("")||txtApellido.getText().equals("")||txtFecha.getText().equals("")||txtContrase単a.getText().equals("")||txtGenero.getText().equals("")) {
         JOptionPane.showMessageDialog(this, "Rellene todos los campos");   
        }else{
        if (txtCedula.getText().length()==10) {
                if (txtContrase単a.getText().length()==8) {
                    Autoridad a= new Autoridad(txtCedula.getText(),txtNombre.getText(),txtApellido.getText(),txtFecha.getText(),txtContrase単a.getText(),txtGenero.getText());
                    ConexionIMatrimonio c = new ConexionIMatrimonio();  
       iMatrimonio c1= c.getConexion("Autoridad");
                    c1.create(a);
               JOptionPane.showMessageDialog(this, "Usuario creado exitosamente");
                    limpiar();
                    setVisible(false);
                }else{JOptionPane.showMessageDialog(this, "La contrase単a debe contar con 8 digitos");
                
                }
            }else{
            JOptionPane.showMessageDialog(this, "La cedula deber contar con 10 digitos");
            }
        
        }
  
    }//GEN-LAST:event_btnRegistarActionPerformed

